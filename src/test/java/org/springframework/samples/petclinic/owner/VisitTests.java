package org.springframework.samples.petclinic.owner;

import org.junit.jupiter.api.Test;

import java.time.LocalDate;

import static org.assertj.core.api.Assertions.assertThat;

/**
 * Unit tests for Visit
 */
class VisitTests {

	@Test
	void testDefaultConstructorSetsDateToToday() {
		Visit visit = new Visit();
		assertThat(visit.getDate()).isEqualTo(LocalDate.now());
	}

	@Test
	void testDateGetterAndSetter() {
		Visit visit = new Visit();
		LocalDate date = LocalDate.of(2025, 11, 11);

		visit.setDate(date);
		assertThat(visit.getDate()).isEqualTo(date);
	}

	@Test
	void testDescriptionGetterAndSetter() {
		Visit visit = new Visit();
		assertThat(visit.getDescription()).isNull();

		visit.setDescription("Annual checkup");
		assertThat(visit.getDescription()).isEqualTo("Annual checkup");
	}

	@Test
	void testDescriptionCanBeChanged() {
		Visit visit = new Visit();
		visit.setDescription("Initial description");
		visit.setDescription("Updated description");

		assertThat(visit.getDescription()).isEqualTo("Updated description");
	}

}
